static inline bool8 IsSpeciesInList(u16 species, const u16 *list, u16 count) { 
    for (u16 i=0;i<count;i++) 
        if (list[i]==species) 
            return TRUE; 
    return FALSE; 
}

static inline bool8 IsSpeciesBannedGlobal(u16 species) {
    if (B_BAN_LEGENDARIES && IsSpeciesInList(species, sBannedLegendaries, ARRAY_COUNT(sBannedLegendaries))) 
        return TRUE;
    if (B_BAN_MYTHICALS && IsSpeciesInList(species, sBannedMythicals, ARRAY_COUNT(sBannedMythicals))) 
        return TRUE;
    if (B_BAN_ULTRA_BEASTS && IsSpeciesInList(species, sUltraBeasts, ARRAY_COUNT(sUltraBeasts))) 
        return TRUE;
    return FALSE;
}

